#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	char	*str;

	i = 0;
	j = 0;
	if (!s)
		return (NULL);
	if (start < ft_strlen(s))
		while (s[i + start] && len > i)
			i++;
	str = (char *)malloc(sizeof(*s) * (i + 1));
	if (!str)
		return (NULL);
	i = -1;
	while (s[++i])
	{
		if (i >= start && j < len)
		{
			str[j] = s[i];
			j++;
		}
	}
	str[j] = 0;
	return (str);
}
