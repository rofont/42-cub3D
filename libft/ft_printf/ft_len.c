int	ft_len(int n, int base)
{
	int	len;

	len = 0;
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	if (n == 0)
		len++;
	while (n)
	{
		len++;
		n /= base;
	}
	return (len);
}
